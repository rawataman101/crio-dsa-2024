package crio.bitwise;

public class BitWiseDemo {
    public static void main(String[] args) {
        //left shift
        System.out.println(1<<2); //1 is binary 2 is shift by 2 bits becomes 100 -> 4
        System.out.println(5<<3); // 2^3 * 5 = 40

        //right shift
        System.out.println(10>>2); //1010 -> 10(binary) = 2(decimal)
    }
}
